#!/usr/bin/env node
// Builds a compact, archetype-grouped card catalog for the balance workflow.
// Merges cards.json (effects/cost/cd) with balance-metrics.json (tags/OPS) so
// agents can design archetype decks and spot off-curve cards quickly.
//
// Outputs:
//   tests/audit/card-catalog.json  — per-card compact records + archetype groups
//   tests/audit/card-catalog.md    — human-skimmable, grouped by archetype × tier

import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, '..');
const cards = JSON.parse(readFileSync(resolve(root, 'src/data/json/cards.json'), 'utf-8')).cards;
const metrics = JSON.parse(readFileSync(resolve(root, 'tests/audit/balance-metrics.json'), 'utf-8'));
const metricById = new Map((metrics.cards || []).map((c) => [c.id, c]));

const ELEMENT_PRIMARY_STAT = {
  attack: 'str', counter: 'str', defense: 'vit', earth: 'vit',
  agility: 'dex', air: 'dex', fire: 'int', water: 'spi',
};

// Detonator = consumes a stack pool for payoff.
function detonatorOf(card) {
  const dets = new Set();
  const walk = (effs) => {
    for (const e of effs || []) {
      if (!e) continue;
      if (e.consume_stack_value) dets.add(e.consume_stack_value);
      if (e.consume_stack) dets.add(e.consume_stack);
      if (e.convert_stack && e.from) dets.add(e.from);
      if (e.then) walk(Array.isArray(e.then) ? e.then : [e.then]);
    }
  };
  walk(card.effects);
  return [...dets];
}

function costStr(cost) {
  if (!cost) return 'free';
  const parts = [];
  if (cost.stamina) parts.push(`${cost.stamina}s`);
  if (cost.mana) parts.push(`${cost.mana}m`);
  return parts.length ? parts.join('+') : 'free';
}

const catalog = cards.map((c) => {
  const m = metricById.get(c.id) || {};
  const elements = c.elements ?? [];
  const primaryStats = [...new Set(elements.map((e) => ELEMENT_PRIMARY_STAT[e]).filter(Boolean))];
  return {
    id: c.id,
    name: c.name,
    tier: c.tier ?? null,
    category: c.category,
    elements,
    primaryStats,
    cost: c.cost ?? null,
    costStr: costStr(c.cost),
    cooldown: c.cooldown,
    exhaust: !!c.exhaust,
    tags: m.tags ?? [],
    detonates: detonatorOf(c),
    conditional: !!m.conditional,
    peakOPS: m.peakOPS ?? null,
    baseOPS: m.baseOPS ?? null,
    drainPerSec: m.drainPerSec ?? null,
    sustainable: m.sustainable ?? null,
    description: c.description,
  };
});

// Archetype groupings (a card can appear in several).
const STACKS = ['poison', 'bleed', 'burn', 'slow', 'stun', 'rage'];
const groups = {};
for (const s of STACKS) groups[s] = [];
groups.armor = [];
groups.heal = [];
groups.pure_damage = [];
groups.detonators = [];
groups.scaling = [];
for (const c of catalog) {
  for (const s of STACKS) if (c.tags.includes(s)) groups[s].push(c.id);
  if (c.tags.includes('armor')) groups.armor.push(c.id);
  if (c.tags.includes('heal')) groups.heal.push(c.id);
  if (c.tags.includes('scaling')) groups.scaling.push(c.id);
  if (c.detonates.length) groups.detonators.push(c.id);
  if (c.tags.includes('damage') && !STACKS.some((s) => c.tags.includes(s)) && !c.tags.includes('armor')) {
    groups.pure_damage.push(c.id);
  }
}

writeFileSync(
  resolve(root, 'tests/audit/card-catalog.json'),
  JSON.stringify({ meta: { count: catalog.length, generated: 'build-card-catalog.mjs' }, groups, cards: catalog }, null, 2),
  'utf-8',
);

// ---- Markdown, grouped by archetype × tier ----
function fmtCard(c) {
  const ops = c.peakOPS != null ? `peakOPS ${c.peakOPS}` : 'OPS n/a';
  const det = c.detonates.length ? ` [det:${c.detonates.join(',')}]` : '';
  const ex = c.exhaust ? ' [exhaust]' : '';
  const cond = c.conditional ? ' [conditional]' : '';
  return `- \`${c.id}\` **${c.name}** (T${c.tier}, ${c.category}, ${c.elements.join('/')}) — cost ${c.costStr}, cd ${c.cooldown}s, ${ops}${det}${ex}${cond} — ${c.description}`;
}

let md = `# Card Catalog (archetype × tier)\n\n`;
md += `Generated by scripts/build-card-catalog.mjs. ${catalog.length} cards. `;
md += `OPS = isolated output-per-second from the balance-metrics model (triage heuristic, NOT a full-combat verdict — use the deck-battle simulator for that).\n\n`;
md += `**Primary-stat by element:** attack/counter→STR, defense/earth→VIT, agility/air→DEX, fire→INT, water→SPI.\n\n`;

const archetypeSections = [
  ['poison', 'Poison appliers'],
  ['bleed', 'Bleed appliers'],
  ['burn', 'Burn appliers'],
  ['slow', 'Slow (soft control)'],
  ['stun', 'Stun (hard control)'],
  ['rage', 'Rage (Fury passive)'],
  ['detonators', 'Detonators / stack consumers'],
  ['armor', 'Armor / defense'],
  ['heal', 'Heal / sustain'],
  ['scaling', 'Stat-gain / scaling'],
  ['pure_damage', 'Pure damage (no stack)'],
];

for (const [key, title] of archetypeSections) {
  const ids = new Set(groups[key]);
  const members = catalog.filter((c) => ids.has(c.id));
  md += `## ${title} (${members.length})\n\n`;
  for (const tier of [1, 2, 3]) {
    const tierCards = members.filter((c) => c.tier === tier).sort((a, b) => (b.peakOPS ?? 0) - (a.peakOPS ?? 0));
    if (!tierCards.length) continue;
    md += `### T${tier}\n`;
    for (const c of tierCards) md += fmtCard(c) + '\n';
    md += '\n';
  }
}

writeFileSync(resolve(root, 'tests/audit/card-catalog.md'), md, 'utf-8');
console.log(`Wrote card-catalog.json (${catalog.length} cards) and card-catalog.md`);
for (const [k, v] of Object.entries(groups)) console.log(`  ${k}: ${v.length}`);
